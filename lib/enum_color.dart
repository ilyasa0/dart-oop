enum Color{red,green,blue,black}

void main(){
  var favColour=Color.red;
  switch (favColour){
    case Color.red:
      print('red is my favorite colour');
      break;
    case Color.green:
      print('green is my favorite colour');
      break;
    case Color.blue:
      print('blue is my favorite colour');
      break;
    case Color.black:
      print('blue is my favorite colour');
      break;
  }
}